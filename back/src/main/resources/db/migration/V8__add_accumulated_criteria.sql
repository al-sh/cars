-- Добавляем поле для накопления критериев поиска в диалоге.
--
-- При каждом сообщении backend мержит новые критерии от LLM с уже накопленными.
-- Это позволяет LLM Extract видеть только текущее сообщение, а не всю историю:
--   "Ранее известно: кроссовер до 3 млн. Текущее сообщение: Бензин, автомат"
--
-- JSONB — бинарный JSON в PostgreSQL:
-- - Быстрее JSON при чтении (парсится при записи, хранится в бинарном виде)
-- - Поддерживает индексацию (GIN/GiST) и операторы @>, ?, ?| и т.д.
-- - NULL при создании нового чата (критериев ещё нет)
ALTER TABLE chats ADD COLUMN accumulated_criteria JSONB;
